#include <stdio.h>

/**
 * main - Entry point
 *
 * Return: Always 0 (Sucess)
 */


int main(void)
{
char lowALpha, upALpha;

for (lowALpha = 'a'; lowAlpha <= 'z'; lowAlpha++)
{
putchar(lowAlpha);
}
for (upALpha = 'a'; upAlpha <= 'z'; upAlpha++)
{
putchar(upAlpha);
}

putchar('\n');

return (0);
}

